def simulate_strategy(data, strategy_type='single_3tan'):
    """指定されたデータと戦略で的中率とROIを計算する"""
    total_races = len(data)
    if total_races == 0:
        return 0, -100, 0
    
    total_cost = total_races * 100
    total_return = 0
    hit_count = 0

    for index, row in data.iterrows():
        is_hit = False
        payout = 0
        
        if strategy_type == 'single_3tan':
            if row['3連単_1着'] == row['3連単_的中']:
                payout = row['3連単_配当金(円)']
                is_hit = True
        
        elif strategy_type == 'single_2tan':
            if row['2連単_予想'] == row['2連単_的中']:
                payout = CONSERVATIVE_2TAN_PAYOUT
                is_hit = True

        if is_hit:
            total_return += payout
            hit_count += 1

    hit_rate = (hit_count / total_races) * 100
    roi = ((total_return - total_cost) / total_cost) * 100
    return hit_rate, roi, total_races

